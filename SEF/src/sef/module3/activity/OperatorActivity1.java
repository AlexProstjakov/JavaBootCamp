public class OperatorActivity1 {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		
		// Declare numbers to be operated1
		int i = 8;
		int j = 5;
		int result;
		
		// Subtract numbers
		if (i>8) {
			result = i-j;
			System.out.println("Difference = " + result);
		}else {
			result = i+j;
			System.out.println("Sum = " + result);
		}
		
	}

}
